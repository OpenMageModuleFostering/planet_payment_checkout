    /**
     * @param $key
     * @return null|string
     */
    protected function _getErrorMessage($key)
    {
        if (isset($this->errorCodes[$key])) {
            return $this->errorCodes[$key];
        }
        elseif ($key != '000.000.000') {
            return $this->errorCodes['default'];
        }
        else {
            return null;
        }
    }

